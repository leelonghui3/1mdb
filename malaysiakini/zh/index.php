						<div id="buyback-8" class="uk-block">
							<p>购回期权阶段的洗钱收费类似之前的阶段，不过它仍有一项关键的差异，即德意志银行的贷款很大一部分用作填补一马公司子公司Brazen Sky有限公司的财务“漏洞”。</p>
							<p>这个“漏洞”源自2009年的<a href="#goodstar" data-uk-smooth-scroll>Good Star阶段</a>，一马公司当时跟沙地石油国家公司有联营计划，理应给联营公司注资18亿3000万美元，但最终10亿3000万美元却遭刘特佐等同伙窃取牟私。</p>
							<p>一马公司于2015年10月10日声称，联营计划结束后的退款，如今以23亿1800万美元“投资单位”形式，放在Brazen Sky手上。一马公司声称，相关“投资单位”包括原先的18亿3000万美元注资，加上4亿8800万美元的盈利。</p>
							<p>不过，美国司法部怀疑说，这些“投资单位”由一个名不见传的公司，即Bridge伙伴投资管理有限公司所管理，而且“相对毫无价值”。</p>
							<p>一马公司从德意志银行借到9亿7500万美元后，其中2亿3300万美元流入阿尔巴塞舌尔公司。钱入账两天后，阿尔巴塞舌尔公司把其中的1亿1100万美元汇给Lambasa全球机会基金。</p>
							<p>Lambasa基金所扮演的角色，跟Enterprise新兴市场基金和Cistenique投资基金类似，即资金中途站。</p>
							<p>Lambasa基金随即把约1亿1000万美元转汇Bridge全球基金。数天后，Bridge全球基金有把约1亿1000万美元汇给Brazen Sky，赎回“投资单位”。</p>
							<p>Brazen Sky接踵把9400万美元汇给一马公司的另一家子公司：一马全球投资有限公司。</p>
							<blockquote class="uk-text-large uk-margin">
								<p>“这种资金流动复杂而且没有必要，其目的在于令人误会，Brazen Sky账户的投资单位已获赎回，转为现金，而且支付予一马公司，进而掩饰这些投资单位根本无法换钱，而且相对没有价值的事实。”</p>
