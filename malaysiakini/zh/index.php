						</div>
						<hr>
						<div id="goodstar-3" class="uk-block">
							<p>截至2011年10月25日，一马公司总计10亿零300万美元转移到Good Star的口袋。而刘特佐利用其中的3500万美元买下英国伦敦的斯特拉顿阁楼（Stratton penthouse）及斯特拉顿公寓（Stratton flat），另外1017万3104美元则投入《华尔街之狼》的电影制作。</p>
							<figure>
								<img src="svg/goodstar/goodstar-3.svg" alt="" width="100%">
							</figure>
							<p>除了刘特佐，分得赃款的还有沙地石油国际公司创办人之一的达列奥拜（Tarek Obaid），他把钱投资在一家称为“Palantir Technologies”的软体工程公司。</p>
							<p>另外，部分资金则透过一个名叫“陈金隆”的神秘人物，分给一马公司的一号领导。</p>
						</div>
						<hr>
						<div id="goodstar-4" class="uk-block">
							<p>2017年1月，新加坡法庭揭露，“<a href="https://www.malaysiakini.com/news/369193" target="_blank">陈金隆</a>”其实就是刘特佐的“<a href="https://www.malaysiakini.com/news/369067" target="_blank">分身</a>”。</p>
