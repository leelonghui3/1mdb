				<p>超级游艇“平静号”今年2月在巴厘岛遭印尼和美国当局扣押，使得渐渐沉寂的美国一马洗钱案，以及美国司法部充公行动再次受到众人的关注。</p>
				<p>美国司法部于2016和2017年多次入禀法院，寻求充公近17亿美元的资产，声称这些资产乃非法挪用一马公司资金所购得，而且涉嫌洗钱。</p>
				<p>美国司法部指控，一马公司领导人和他们的同伙于2009至2014年间，窃取一马公司的45亿美元，同时通过全球——包括美国的金融体系洗钱。这是美国司法部认为，相关资金遭窃取私用，因此在“追回盗窃资产行动”下展开史上最广泛的调查和充公动作。</p>
				<p>根据美国司法部，一马资金被挪用购买各种的奢华房地产、艺术品、私人飞机、珠宝，资助电影制作公司，进而拍摄出《华尔街之狼》等电影。而据称属于大马富豪刘特佐的“平静号”也是充公清单上的资产之一。</p>
				<p>根据美国司法部诉状的叙述，刘特佐是整个洗钱阴谋的主脑。诉状也点名了其他人，包括刘特佐的好友兼首相纳吉的继子里扎、多名一马公司领导，以及据称就是纳吉本人的“一号大马官”。</p>
				<p>美国多名闻人，包括好莱坞明星里奥纳多，以及名模米兰达（Miranda Kerr）都卷入其中。</p>
				<p>美国司法部诉状把整个横跨5年时间的洗钱过程，细分为4个阶段，包括“Good Star阶段”、“阿尔巴BVI阶段”、“Tanore阶段”和“购回期权阶段”。</p>
				<p>相关诉状长达数百页，其中详尽地交代了这宗一马洗钱案的复杂钱流网。本文尝试化繁为简，带领读者追踪这一系列错综复杂的洗钱过程。</p>
