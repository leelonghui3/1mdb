						<div id="goodstar-1" class="uk-block">
							<p>根据美国司法部，一马公司于2009年9月创立后不久，它的逾10亿美元资金就遭人诈取。</p>
							<p>2009至2011年间，一马公司跟沙地阿拉伯一家私人石油开采公司——沙地石油国际公司（PetroSaudi International）签订联营协议。</p>
							<p>据称，沙地石油国际公司在土库曼斯坦和阿根廷握有特许的能源经营权，而一马公司与它的联营就是为了分一杯羹。</p>
							<p>双方协议下，一马公司给联营公司投下10亿美元的资金，换得联营公司的40%股权。惟事实上，最终只有3亿美元进入联营公司的账户，其余7亿美元流入刘特佐所拥有的Good Star有限公司。</p>
							<figure>
								<img src="svg/goodstar/goodstar-1.svg" alt="" width="100%">
							</figure>
							<p>美国司法部声称，代号“一号领导”和“二号领导”的一马公司领导蒙骗银行，仍他们误以为Good Star是沙地石油国际公司的子公司。另一边厢，一马公司董事局却受误导，以为7亿美元用以清还了联营公司拖欠沙地石油国际公司的债务。</p>
							<p>虽然刘特佐在一马公司没有正式职位，但他却是一马公司前身——登嘉楼投资机构（Terengganu Investment Authority）创立时的顾问。联邦政府在2009年初接管登嘉楼投资机构，并把它易名为一马公司。</p>
							<p>刘特佐在唸大学时认识了纳吉继子里扎，而且透过后者，跟纳吉家庭攀上关系。</p>
						</div>
						<hr>
						<div id="goodstar-2" class="uk-block">
							<p>2010年6月，一马公司决定卖掉它在联营公司的40%股权，不过它最终换回的不是现金，而是据说包含盈利的12亿美元债券。</p>
