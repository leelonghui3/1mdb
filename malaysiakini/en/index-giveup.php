				<div id="aabarbvi">
					<div id="aabarbvi-1" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
						<h1 class="uk-visible@s" style="background-color: #FFF3E0;">Aabar-BVI Phase</h1>
	
						<div class="firstpara-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-1.svg" width="100%"></object>
						</div>
	
						<p>In 2012, 1MDB raised US$3.5 billion in two separate bond offerings to acquire energy assets in Malaysia but a substantial portion of the proceeds - approximately US$1.367 billion - was misappropriated.</p>
	
						<p>The bonds were guaranteed by International Petroleum Investments Company (IPIC), an investment entity owned by Abu Dhabi government.</p>
	
						<p>In exchange, 1MDB agreed to provide IPIC’s subsidiary Aabar Investments PJS (Aabar) options to acquire a 49 percent interest in an energy asset involved.</p>
					</div>
	
					<div id="aabarbvi-2" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-2.svg" width="100%"></object>
						</div>
	
						<p>1MDB received the proceeds of each of the bond issue in 2012, but the proceeds were almost immediately transferred to an entity called Aabar Investments PJS Limited (Aabar-BVI), a British Virgin Islands-registered corporation.</p>
	
						<p>Aabar-BVI bore a similar name to the IPIC’s Aabar, but there was no affiliation between IPIC or Aabar-BVI. 1MDB later conceded that 1MDB might been a "<a href="https://www.malaysiakini.com/news/337682" target="_blank">victim of fraud</a>".</p>
					</div>
	
					<div id="aabarbvi-3" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-3.svg" width="100%"></object>
						</div>
	
						<p>At the time, Khadem Abdulla al-Qubaisi was the managing director of IPIC and the chairperson of Aabar. Mohamed Ahmed Badawy Al-Husseiny was the CEO of Aabar. However, both of them were also the director of Aabar-BVI.</p>
	
					<blockquote uk-scrollspy="cls: 'uk-animation-slide-right', repeat: true">
						<p>"Khadem and Mohamed opened the account at BSI Bank in Lugano in the name of Aabar-BVI and used the account to facilitate the diversion of funds from 1MDB. Low was also integrally involved in setting up the Aabar-BVI Swiss Account and in arranging
							for the fraudulent transfer of $1.367 billion from 1MDB to the Aabar-BVI Swiss Account."</p>
								<small>The DOJ</small>
						</blockquote>
	
					</div>
	
					<div id="aabarbvi-4" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-4.svg" width="100%"></object>
						</div>
	
						<p>A total of US$1.092 billion from Aabar-BVI was diverted through two overseas investments funds - Enterprise Emerging Market Funds (Enterprise) and Cistenique Investment Fund (Cistenique) - and transferred to Blackstone Asia Real Estate Partners
							(Blackstone) which is owned by Eric Tan (Jho Low).</p>
					</div>
	
					<div id="aabarbvi-5" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-5.svg" width="100%"></object>
						</div>
	
						<p>Two companies owned by Mohamed - Rayan Inc and MB Consulting LLC - received US$11.6 million and US$55 million from Blackstone respectively, while Vasco Investment owned by Khadem got US$472,750,000, which he later used to purchase the Walker
							Tower Penthouse, Laurel Beverly Hills Mansion and a Hillcrest property.</p>
					</div>
	
					<div id="aabarbvi-6" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-6.svg" width="100%"></object>
						</div>
	
						<p>MO1 also received US$30 million from Blackstone. The former 1MDB general counsel and executive director of group strategy Jasmine Loo got US$5 million which she used to purchase a residential unit at One Madison Park Condominium.</p>
					</div>
	
					<div id="aabarbvi-7" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-7.svg" width="100%"></object>
						</div>
	
						<p>Another US$238 million from Aabar-BVI was wired to Red Granite Capital, an entity founded by Riza Shahriz Abdul Aziz. As mentioned earlier, Riza is the stepson of Najib as described by the DOJ that Riza is a relative of MO1.</p>
					</div>
	
					<div id="aabarbvi-8" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-8.svg" width="100%"></object>
						</div>
	
						<p>Riza then channelled the money to film production company Red Granite Pictures to produce films such as The Wolf of Wall Street, Daddy’s Home and Dumb and Dumber To.</p>
	
						<p>The production of The Wolf of Wall Street received over US$60 million in the <a href="#goodstar" uk-smooth-scroll>Good Star phase</a> and Aabar-BVI phase. Red Granite Pictures <a href="https://www.malaysiakini.com/news/414701" target="_blank">reached a settlement</a> with the US government
							to the tune of US$60 million, and Riza will only draw a limited salary to cover his health insurance until the full settlement amount is paid.</p>
					</div>
	
					<div id="aabarbvi-9" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/aabarbvi/aabarbvi-9.svg" width="100%"></object>
						</div>
	
						<p>A sum of US$94.3 million was sent from Red Granite Capital to Shearman IOLA to acquire several luxury properties. In particular, Riza spent US$17.5 million and US$33.5 million to buy a Hillcrest property and Park Laurel Condominium from Jho
							Low, who had purchased the properties in <a href="#goodstar" uk-smooth-scroll>Good Star phase</a>.</p>
					</div>
	
