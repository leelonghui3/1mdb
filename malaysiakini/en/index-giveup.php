				<div id="goodstar" class="uk-width-2-5">
	
					<div id="goodstar-1">
	
						<p>According to the DOJ, more than US$1 billion was fraudulently diverted from 1MDB in September 2009, soon after its creation.</p>
	
						<img src="svg/goodstar/goodstar-1.svg" alt="" class="uk-alert">
	
						<p>Between 2009 and 2011, 1MDB had entered a joint-venture agreement with PetroSaudi International (PSI), a private Saudi oil extraction company.</p>
	
						<p>The joint venture (1MDB-PSI) aimed to exploit energy concessions PSI purportedly owned in Turkmenistan and Argentina.</p>
	
						<p>Under the agreement, 1MDB had invested US$1 billion cash in the joint venture in exchange for a 40 percent equity interest. However, only US$300 million went into the joint venture’s account while the remaining US$700 million went to Good Star
							Limited, a company belonging to Jho Low.</p>
	
						<p>Two officials, who were named by DOJ as 1MDB Officer 1 and 2, had misrepresented to banks that Good Star was a subsidiary of PSI. 1MDB’s board of director was told that the US$700 million was used to settle the debt owed by the joint venture
							to PSI.
						</p>
	
						<p>Although Jho Low had no official position in 1MDB, he was an adviser in the formation of 1MDB’s predecessor, the Terengganu Investment Authority (TIA). The investment arm of Terengganu, a Malaysian state, was taken over by the federal government
							and renamed as 1MDB in early 2009.</p>
	
						<p>Jho Low built his relationship with Najib’s family through Riza, whom he has known since his days in university.</p>
					</div>
	
					<div id="goodstar-2">
						<p>In June 2010, 1MDB had decided to dispose of its 40 percent equity interest in the joint venture. Instead of taking cash for the stake sale, 1MDB settled on US$1.2 billion in debt notes, which included purported profits.</p>
						<img src="svg/goodstar/goodstar-2.svg" alt="" class="uk-alert">
						<p>Three months later, 1MDB-PSI joint venture received an additional loan of US$500 million from 1MDB. In May 2011, 1MDB had issued an extra US$330 million credit to the joint venture, but the money ended up in Good Star’s account.</p>
					</div>
	
					<div id="goodstar-3" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-3.svg" width="100%"></object>
						</div>
	
						<p>As of 25 Oct 2011, a total of US$1.03 billion of 1MDB’s funds was diverted to Good Star. Low used US$35 million of it to purchase the Stratton Penthouse and the Stratton Flat in London, while diverted US$10,173,104 for the production of the movie The Wolf of Wall Street.</p>
	
						<p>Another beneficiary was PSI co-founder Tarek Obaid who used the money to acquire shares of a software engineering company Palantir Technologies.</p>
	
						<p>Some of the money was distributed to 1MDB Officer 1, through an individual called Eric Tan.</p>
					</div>
	
					<div id="goodstar-4" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-4.svg" width="100%"></object>
						</div>
	
						<p>In Jan 2017, a Singapore court disclosed that <a href="https://www.channelnewsasia.com/news/singapore/1mdb-probe-falcon-bank-s-former-branch-manager-sentenced-to-28-w-7581388" target="_blank">Eric Tan</a> was, in fact, an alias for Jho Low.</p>
					</div>
	
					<div id="goodstar-5" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-5.svg" width="100%"></object>
						</div>
	
						<p>Malaysian Official 1 (MO1), too, received US$20 million in this phase. The DOJ described MO1 as a "high-ranking official in the Malaysian government who also held a position of authority with 1MDB".</p>
					</div>
	
					<div id="goodstar-6" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-6.svg" width="100%"></object>
						</div>
	
						<p>Malaysian minister Abdul Rahman Dahlan had said that <a href="http://www.bbc.co.uk/news/business-37234717" target="_blank">MO1 is Najib</a>, according to his interview with BBC in 2016. Najib had repeatedly denied he had involved in any wrongdoing
							in the 1MDB saga.</p>
					</div>
	
					<div id="goodstar-7" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-7.svg" width="100%"></object>
						</div>
	
						<p>Jho Low tried to mask the acquisition of assets using the ill-gotten funds in Good Star by going through law firms as well as a web of companies and his father.</p>
	
						<p>Between 21 Oct 2009 and 13 Oct 2010, more than US$368 million in 11 wires was moved from Good Star to an Interest on Lawyer Account held by law firm Shearman & Sterling LLP (Shearman IOLA) in the United States.</p>
	
						<p>Subsequently, the money was used to purchase luxury properties, a hotel and a private jet. They included the L'Ermitage Hotel, a Hillcrest property in Beverly Hill, a Park Laurel Condominium unit, and a Bombardier Global 5000 aircraft worth
							millions of US dollars respectively.</p>
					</div>
	
					<div id="goodstar-8" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-8.svg" width="100%"></object>
						</div>
	
						<p>The DOJ also found that Jho Low purchased assets using his father’s bank account, Larry.</p>
	
						<p>Good Star transferred approximately US$508 million to Abu Dhabi-Kuwait-Malaysia Investment Corporation (ADKMIC) between 28 June 2011 and 4 Sept 2013. Low owned ADKMIC for money laundering, DOJ claimed. Good Star’s account was closed on 5 Sept
							2013 after the transactions.</p>
	
						<p>Following this, ADKMIC transferred about US$327 million to Larry in three tranches in 2011 and 2012. Larry then wired the funds to an account in the name of Selune Ltd (US$30 million) and Jho Low (US$268 million) respectively.</p>
	
						<p>Selune subsequently wired US$27 million to Shearman IOLA. The money was used as part of the funds to purchase Time Warner Penthouse.</p>
					</div>
	
					<div id="goodstar-9" uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-9.svg" width="100%"></object>
						</div>
	
						<p>Meanwhile, Jho Low used the money from Larry to acquire shares in EMI Music Publishing Group America Holdings Inc (EMI) with US$106,666,667 and transferred US$146,246,670 to Selune. US$36,246,670 of the US$146,246,670 was proceeds of the sale
							of Qentas Townhouse to Riza in <a href="#aabarbvi" uk-smooth-scroll>Aabar-BVI phase</a>.</p>
	
						<p>In acquiring stakes in EMI, the DOJ noted that Low represented that family resources were a significant source of his wealth.</p>
	
						<blockquote uk-scrollspy="cls: 'uk-animation-slide-left', repeat: true">
							<p id="goodstar-10">"By funnelling money through his father’s account for a brief period of time, LOW created the appearance that funds in his personal account, which were used to acquire an interest in EMI Partner A, came from his father rather than from Good
							Star or ADKMIC."</p>
							<small>The DOJ</small>
						</blockquote>
	
					</div>
					<div uk-parallax="opacity:0,1; viewport:0.4; y:50%">
	
						<div class="para-padding-top" style="z-index: -1;">
							<object type="image/svg+xml" data="svg/goodstar/goodstar-10.svg" width="100%"></object>
						</div>
	
						<p>Selune Ltd then passed on the funds to Shearman IOLA for the purchase of a Time Warner Penthouse. Jho Low and Selune Ltd collectively sent US$51,682,800 to an US-based law firm DLA Piper for the purchase of the Oriole Mansion, the Greene Condominium
							and to acquire interest in Viceroy Hotel Group with the funds from Shearman IOLA.</p>
					</div>
				</div>
	
