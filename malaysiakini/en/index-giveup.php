		<!-- Introduction -->
		<section class="uk-section uk-section-secondary">
			<div class="uk-container uk-container-small">
				<p>The seizure of The Equanimity luxury yacht in Bali, Indonesia, in late February this year, has once again brought the civil forfeiture suits by the US Department of Justice (DOJ) into the spotlight.</p>
	
				<p>The DOJ had filed a series of the forfeiture suits in 2016 and 2017, seeking to seize assets worth nearly US$1.7 billion allegedly acquired using money siphoned off from the Malaysian fund 1Malaysia Development Berhad (1MDB).</p>
	
				<p>According to the DOJ, 1MDB officials and their associates had embezzled US$4.5 billion between 2009 and 2014, which was allegedly laundered through the global financial systems including the US. It is the most extensive action brought under its
					Kleptocracy Asset Recovery Initiative. The department said that the money was used for personal gain.</p>
	
				<p>They include purchasing luxury properties, artworks, a private jet, jewellery, funding movie production company which produced movies like "The Wolf of Wall Street". The Equanimity, which is said to belong to Malaysian businessperson Jho Low, was
					one of the assets on the list.</p>
	
				<p>Jho Low, whose full name is Low Taek Jho, was portrayed as the mastermind of the money laundering scheme in the DOJ lawsuit. Several other persons were named in the lawsuits, including Jho Low’s friend Riza Aziz, who is also the stepson of Malaysian
					Prime Minister Najib Abdul Razak, several 1MDB officers, and an unnamed "Malaysian Official 1", who was said to be Najib himself.</p>
	
				<p>Several celebrities, including Hollywood actor Leonardo Dicaprio and model Miranda Kerr were dragged into the matter.</p>
	
				<p>The lawsuits had categorised the money-laundering processes over the five-year period into four phases, namely the "Good Star phase", "Aabar BVI phase", "Tanore phase" and "Options Buyback phase".</p>
	
				<p>The lawsuits consist several hundred pages long and provide a blow-by-blow account of the complex series of transaction. This article will break down the complexities of the suits and walk readers through the money trail.</p>
			</div>
