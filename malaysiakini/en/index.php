						<h2 class="uk-article-title uk-text-bold" style="padding-left:15px;border-left:10px solid #A5D6A7;">Money Cycle</h2>
						<div id="buyback-8" class="uk-block">
							<p>The money laundering methods used in Options Buyback phase are similar to the previous stages, but there was one key difference. A significant amount of the Deutsche Bank loan was also used to cover the "hole" in 1MDB’s subsidiary Brazen Sky Limited.</p>
							<p>It can be traced back to the money trail in <a href="#goodstar" data-uk-smooth-scroll="{top:50}">Good Star phase</a> in 2009, where 1MDB had supposedly invested US$1.83 billion to the 1MDB-PSI joint venture but US$1.03 billion of it was allegedly siphoned for the personal benefit of Jho Low and his associates.</p>
							<p>On Oct 10, 2015, 1MDB claimed that Brazen Sky was holding a US$2.318 billion in "fund units" which was redeemed from the joint venture. 1MDB claimed the "fund units" comprised its initial US$1.83 billion investment which was redeemed on top of a US$488 million profit.</p>
							<p>The DOJ claimed the "fund units" managed by little-known Bridge Partners Investment Management Limited, were "relatively worthless".</p>
							<p>Two days after Aabar-Seychelles received US$233 million of a US$975 million loan from Deutsche Bank intended for 1MDB, it transferred US$111 million to Lambasa Global Opportunity Fund.</p>
							<p>Lambasa’s function was similar to Enterprise and Cisenique, which was to function as a pass-through entity.</p>
							<p>Almost immediately after that, Lambasa passed about US$110 million to Bridge Global Fund. A few days later, Bridge Global Fund transferred approximately US$110 million to Brazen Sky as "redemption" of fund units.</p>
							<p>It was followed by Brazen Sky sending approximately US$94 million to 1MDB Global Investments Limited, another subsidiary of 1MDB.</p>
							<blockquote class="uk-text-large uk-margin">
								<p>"The purpose of this unnecessarily complicated funds flow was to create the appearance that fund units in the Brazen Sky Account were being redeemed for cash and being paid forward to 1MDB, thereby fraudulently disguising the fact that the fund units were illiquid and relatively worthless."</p>
								<small>The DOJ</small>
							</blockquote>
							<p>The same pattern of fund flows was repeated on Sept 29, 2014, after Aabar-Seychelles received the second tranche of loan proceeds from Deutsche Bank. This time, the majority of the funds that transited through Brazen Sky were returned to Aabar-Seychelles rather than being retained by 1MDB Global Investments. The cycle was repeated for another five times.</p>
							<blockquote class="uk-text-large uk-margin">
								<p>"This allowed the funds to be recycled again and again through the same circle of entities, thereby creating the appearance of multiple ‘redemptions’ using the same pool of money."</p>
								<small>The DOJ</small>
							</blockquote>
							<p>By the end of November 2014, proceeds from the first tranche of the loan had passed through Brazen Sky once and the second tranche of the loan had passed through Brazen Sky six times. Finally, it appeared as if Brazen Sky had redeemed US$1.412 billion from its "fund units".</p>
